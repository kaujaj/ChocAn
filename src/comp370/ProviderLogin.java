    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        int pronum= Integer.parseInt(usertxt.getText());//Provider Number
        String passwd= passtxt.getText();//Pssword
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost/comp370","KJ","1996");
            Statement st = con.createStatement();
            ResultSet rs= st.executeQuery("select * from provider where provider_num="+pronum);
            if(rs.next()){
                if(rs.getString("Password").matches(passwd)){//Check From Database
                    UserCardSwipe cs= new UserCardSwipe(pronum);
                    setVisible(false);
                    cs.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Wrong ID or Password");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Wrong ID or Password");
            System.out.println("Exception");
        }       
    }//GEN-LAST:event_loginbtnActionPerformed

